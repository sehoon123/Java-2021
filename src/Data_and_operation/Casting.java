package Data_and_operation;

public class Casting {
    public static void main(String[] args) {
        double a = 1.1;
        double b = 1;
        double b2 = (double) 1;

        System.out.println(b);

        int c = (int) 1.1;
        System.out.println(c);

        String i = Integer.toString(1);
        System.out.println(i);
        System.out.println(i.getClass());

    }
    
}
